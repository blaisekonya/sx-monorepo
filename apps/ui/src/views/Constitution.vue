Article 16 - World Territories and
Exterior Relations
16.1. - World Territory
16.1.1. Those areas of the Earth and Earth’s
moon that are not under the jurisdiction of
existing nations at the time of forming the Earth
Federation, or that are not reasonably within the
province of national ownership and
administration, or that are declared to be World
Territory subsequent to establishment of the
Earth Federation, are World Territory and belong
to all of the people of Earth.
16.1.2. The World Parliament shall determine
the administration of World Territory. and
implemented by The World Executive shall
implement the administration of World Territory.
World Territory applies to the following areas:
16.1.2.1. All oceans and seas having an
international or supra-national character,
together with the sea beds and resources
thereof, beginning at a distance of twenty
kilometers offshore, excluding inland seas of
traditional national ownership.
16.1.2.2. Vital straits, channels, and canals.
16.1.2.3. The atmosphere enveloping Earth,
beginning at an elevation of one kilometer above
the general surface of the land, excluding the
depressions in areas of much variation in
elevation.
16.1.2.4. Man-made satellites and Earth’s moon.
16.1.2.5. Colonies that might choose the status
of World Territory; non-independent territories
under the trust administration of nations or of the
United Nations; any islands or atolls that are
unclaimed by any nation; independent lands or
countries that choose the status of World
Territory; and disputed lands that choose the
status of World Territory.
16.1.3. The residents of any World Territory,
except designated World Federal Zones, have a
prerogative within reason to decide by plebiscite
to become a self-governing nation within the
Earth Federation, either singly or in combination
with other World Territories, or to unite with an
existing nation of the Earth Federation.
16.2. - Exterior Relations
16.2.1. The Earth Federation shall maintain
exterior relations with those nations of Earth that
have not joined the Earth Federation. Exterior
relations are under the administration of the
Presidium, subject at all times to World
Parliament specific instructions and approval.
16.2.2. The Presidium shall negotiate all treaties
and agreements with nations remaining outside
the Earth Federation. Treaties and agreements
are subject to ratification by a simple majority
vote of the three Houses of the World
Parliament.
16.2.3. The Earth Federation shall establish and
maintain peaceful relations with other planets
and celestial bodies if communications establish
with the possible inhabitants thereof.
16.2.4. All explorations into outer space, both
within and beyond the solar system in which
Planet Earth is located, are under the exclusive
direction and control of the Earth Federation.
The World Parliament shall determine the
manner in which to conduct explorations.
