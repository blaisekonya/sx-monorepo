to compose the Presidium and the Executive
Cabinet for the Provisional Earth Federation.
19.2.09. The Finance Commission shall work on
ways and means for financing the Provisional
Earth Federation.
19.2.10. The several commissions on particular
world problems shall work on the preparation of
proposed world legislation and action on each
problem, to present to the Provisional World
Parliament when it convenes.
19.3. - Composition of the Provisional
World Parliament
19.3.1. The Provisional World Parliament
composes of the following members:
19.3.1.1. All those who were accredited as
delegates to the 1977 and 1991 Sessions of the
World Constituent Assembly, as well as to any
previous Session of the Provisional World
