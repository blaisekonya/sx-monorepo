19.3.1.2. Persons who obtain the required
number of signatures on election petitions, or
who are designated by non-governmental
organizations that adopt approved resolutions
for this purpose, or who are otherwise
accredited according to terms specified in Calls
that might issue to convene particular sessions
of the Provisional World Parliament.
19.3.1.3. Members of the World Parliament to
the House of Peoples who are elected from
World Electoral and Administrative Districts up
to the time of convening the Provisional World
Parliament. Members of the World Parliament
elected to the House of Peoples may continue to
be added to the Provisional World Parliament
until the Earth Federation attains the first
operative stage.
19.3.1.4. Members of the World Parliament to
the House of Nations who are elected by
national legislatures or appointed by national
governments up to the time of convening the
