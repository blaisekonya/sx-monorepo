House of Peoples or to the House of
Counsellors, or appointed or elected to the
House of Nations.
6.3.5. The House of Counsellors shall make
nominations for the Presidium, with number of
nominees to be from two to three times the
number to be elected. No more than one-third of
the nominees must come from the House of
Counsellors or from the House of Nations. The
House of Counsellors shall include nominees
from all Continental Divisions.
6.3.6. From among the nominees submitted by
the House of Counsellors, the combined
membership of all three houses of the World
Parliament in joint session shall elect the
Presidium. The election of each member to the
Presidium is subject to a plurality vote equal to
at least 40 percent of the total membership of
the World Parliament, with successive
elimination votes taken as necessary until the
required plurality is achieved.
6.3.7. An absolute majority vote of the combined
membership of the three houses of the World
Parliament in joint session may remove for
cause Members of the Presidium, either
individually or collectively.
6.3.8. The term of office for the Presidium is five
years and runs concurrently with the terms of
office for the members as Members of the World
Parliament, except that at the end of each fiveyear period, the Presidium members in office
shall continue to serve until the World
Parliament elects the new Presidium for the
succeeding term. Term limits for Membership in
the Presidium are two consecutive terms.
6.4. The Executive Cabinet
6.4.1. From twenty to thirty members, with at
least one member from each of the ten World
Electoral and Administrative Magna Regions of
the world, compose the Executive Cabinet.
6.4.2. World Parliament, from among its own
Members, elects the members of the Executive
Cabinet.
6.4.3. No more than two members of the
Executive Cabinet originate from any single
nation of the Earth Federation. There may be
only one member of the Executive Cabinet from
a nation from which a Member of the World
Parliament is serving as a member of the
Presidium.
6.4.4. Each member of the Executive Cabinet
shall serve as the head of a department or
agency of the World Administration or
Integrative Complex. Presidium shall designate
a Minister for each department or agency.
6.4.5. The Presidium shall make nominations for
members of the Executive Cabinet, taking into
consideration the various functions that
Executive Cabinets members are to perform.
The Presidium shall nominate no more than two
times the number to be elected.
6.4.6. Simple majority vote of the combined
membership of all three houses of the World
Parliament in joint session elects the Executive
Cabinet.
6.4.7. An absolute majority vote of the combined
membership of all three houses of the World
Parliament sitting in joint session may remove
for cause Members of the Executive Cabinet,
either individually or collectively.
6.4.8. The term of office in the Executive
Cabinet is five years. Executive Cabinet terms
run concurrently with the terms of office for the
members as Members of the World Parliament,
except that at the end of each five year period,
the Cabinet members in office shall continue to
serve until the World Parliament elects the new
Executive Cabinet for the succeeding term.
Term limits for membership in the Executive
Cabinet are three consecutive terms, regardless
of change in ministerial position.
6.5. Procedures of the World
Executive
6.5.1. The Presidium shall assign the ministerial
positions among the Cabinet members to head
the several administrative departments and
major agencies of the Administration and of the
Integrative Complex. Each Vice President may
also serve as a Minister to head an
administrative department, but not the President.
At the discretion of the Presidium, the Presidium
may change Ministerial positions. A Cabinet
member or Vice President may hold more than
one ministerial post, but no more than three,
providing that no Cabinet Member is without a
Ministerial post.
13
6.5.2. The Presidium, in consultation with the
Executive Cabinet, shall prepare and present to
the World Parliament near the beginning of each
year a proposed program of world legislation.
The Presidium may propose other legislation
during the year.
6.5.3. The Presidium, in consultation with the
Executive Cabinet, and in consultation with the
World Financial Administration, (see Article
8.7.1.9.) is responsible for preparing and
submitting to the World Parliament the proposed
annual budget, and budgetary projections over
periods of years.
6.5.4. Each Cabinet Member and Vice President
as Minister of a particular department or agency
shall prepare an annual report for the particular
department or agency, submitting to both the
Presidium and to the World Parliament.
6.5.5. The members of the Presidium and of the
Executive Cabinet at all times are responsible
both individually and collectively to the World
Parliament.
6.5.6. Within sixty days, World Parliament shall
fill vacancies occurring at any time in the World
Executive by nomination and election in the
same manner as specified for filling the offices
originally.
6.6. Limitations on the World
Executive
6.6.1. The World Executive must not at any time
alter, suspend, abridge, infringe or otherwise
violate any provision of this World Constitution
or any legislation or world law enacted or
approved by the World Parliament in
accordance with the provisions of this World
Constitution.
6.6.2. The World Executive does not have veto
power over any legislation passed by the World
Parliament.
6.6.3. The World Executive must not attempt to
and cannot dissolve the World Parliament or any
House of the World Parliament.
6.6.4. The World Executive must not act
contrary to decisions of the World Courts.
6.6.5. The World Executive is bound to faithfully
execute all legislation passed by the World
Parliament in accordance with the provisions of
this World Constitution. The World Executive
must not impound or refuse to spend funds
appropriated by the World Parliament, nor spend
more funds than are appropriated by the World
Parliament.
6.6.6. The World Executive must not attempt to
transcend or contradict the decisions or controls
of the World Parliament, the World Judiciary or
the Provisions of this World Constitution by any
device of executive order or executive privilege
or emergency declaration or decree.

Article 7 - The World Administration
7.1. - Functions of the World Administration
7.1.1. The World Administration organizes to
carry out the detailed and continuous
administration and implementation of world
legislation and world law.
7.1.2. The World Administration is under the
direction of the World Executive, and is at all
times responsible to the World Executive.
7.1.3. The World Administration organizes to
give professional continuity to the work of
administration and implementation.
7.2. - Structure and Procedures of the
World Administration
7.2.01. Professionally organized departments
and other agencies in all areas of activity
requiring continuity of administration and
implementation by the Earth
Federation compose the World Administration.
7.2.02. A Minister who is either a member of the
Executive Cabinet or a Vice President of the
Presidium heads each Department or major
agency of the World Administration.
7.2.03. Each Department or major agency of the
World Administration has as chief of staff a
Senior Administrator, who shall assist the
Minister and supervise the detailed work of the
Department or agency.
7.2.04. From among persons in the senior lists
of the World Civil Service Administration, as
soon as the World Civil Service Administration
establishes senior lists, the Minister of each
particular Department or agency shall nominate
a respective Senior Administrator, subject to
confirmation by the Presidium. Ministers shall
make temporary qualified appointments, subject
to confirmation by the Presidium, pending
establishment of the senior lists.
7.2.05. The Presidium shall nominate a
Secretary General of the World Administration,
subject to confirmation by absolute majority vote
of the entire Executive Cabinet.
