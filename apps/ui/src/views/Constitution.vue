Article 9 - The World Judiciary
9.1. - Jurisdiction of the World
Supreme Court
9.1.1. A World Supreme Court performs the
world judicial function, together with Regional
and District World Courts as World Parliament or
the Court decides necessary. The World
Supreme Court comprises a number of benches.
9.1.2. Under the provisions of this World
Constitution, world legislation, and the body of
world law approved by the World Parliament, the
World Supreme Court, together with Regional
and District World Courts, have mandatory
jurisdiction in all the following world
circumstances: cases, actions, disputes,
conflicts, violations of law, civil suits, guarantees
of civil and human rights, constitutional
interpretations, and other litigations.
9.1.3. Decisions of the World Supreme Court are
binding on all parties involved in all cases,
actions and litigations brought before any bench
of the World Supreme Court for settlement.
Each bench of the World Supreme Court
constitutes a court of highest appeal, except
when a World Supreme Court bench assigns or
transfers cases regarding matters of extraordinary public importance to the Superior
Tribunal of the World Supreme Court, as
provided in Article 9.5 of this World Constitution.
9.2. - Benches of the World Supreme
Court
The benches of the World Supreme Court and
their respective jurisdictions are as follows:
9.2.01. Bench for Human Rights: To deal with
issues of human rights arising under the
guarantee of civil and human rights provided by
Article 12 of this World Constitution, and arising
in pursuance of the provisions of Article 13 of
this World Constitution, and arising otherwise
under world legislation and the body of world law
approved by the World Parliament.
20
9.2.02. Bench for Criminal Cases: To deal with
issues arising from the violation of world laws
and world legislation by individuals,
corporations, groups and associations, but not
issues primarily concerned with human rights.
9.2.03. Bench for Civil Cases: To deal with
issues involving civil law suits and disputes
between individuals, corporations, groups and
associations arising under world legislation and
world law and the administration thereof.
9.2.04. Bench for Constitutional Cases: To deal
with the interpretation of the World Constitution
and with issues and actions arising in
connection with the interpretation of the World
Constitution.
9.2.05. Bench for International Conflicts: To deal
with disputes, conflicts and legal contest arising
between or among the nations which have
joined in the Earth Federation.
9.2.06. Bench for Public Cases: To deal with
issues not under the jurisdiction of another
bench arising from conflicts, disputes, civil suits
or other legal contests between the World
Government and corporations, groups or
individuals, or between national governments
and corporations, groups or individuals in cases
involving world legislation and world law.
9.2.07. Appellate Bench: To deal with issues
involving world legislation and world law which
may be appealed from national courts; and to
decide which bench to assign a case or action or
litigation when a question or disagreement
arises over the proper jurisdiction.
9.2.08. Advisory Bench: To give opinions upon
request on any legal question arising under
world law or world legislation, exclusive of
contests or actions involving interpretation of the
World Constitution. Parts of the Earth Federation
may request advisory opinions: Any House or
committee of the World Parliament, the
Presidium, any Administrative Department, the
Office of World Attorneys General, the World
Ombudsmus, or by any agency of the Integrative
Complex.
9.2.09. Upon recommendation of the Collegium
of World Judges, World Parliament may
establish, combine or terminate other benches;
but must not combine or terminate benches
number one through nine except by amendment
of this World Constitution.
9.3. - Seats of the World Supreme
