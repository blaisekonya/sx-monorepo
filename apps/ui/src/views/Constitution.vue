2.4, compose the House of Peoples.
5.3.2. Under conditions of universal adult
suffrage open to all natural persons of age 18
and above, the People shall elect Peoples
delegates.
5.3.3. From each World Electoral and
Administrative District, the people shall elect one
peoples delegate to serve a five-year term in the
House of Peoples. People may elect Peoples
delegates to serve successive terms without
limit. Each peoples delegate has one vote.
5.3.4. A candidate for election to serve as a
peoples delegate must be at least 21 years of
age, a resident for at least one year of the
electoral district from which the candidate is
seeking election, and shall take a pledge of
service to humanity.
5.4. - The House of Nations
5.4.1. Each national government determines
respective national procedures to elect or
appoint respective National Delegates to
compose the House of Nations on the following
basis:
5.4.1.1. One national delegate from each nation
of at least 100,000 in population, but less than
10,000,000 in population.
5.4.1.3. Two national delegates from each
nation of at least 10,000,000 in population, but
less than 100,000,000 in population.
5.4.1.4. Three national delegates from each
nation of 100,000,000 in population or more.
5.4.2. Nations of less than 100,000 in population
may join in groups with other nations for
purposes of representation in the House of
Nations.
5.4.3. People of Nations shall elect or
governments of nations shall appoint or elect
National delegates for the respective nation to
serve for terms of five years. Source may elect
or appoint National delegates to serve
successive terms without limit. Each national
delegate has one vote.
5.4.4. Requirements to serve as a national
delegate are citizenship for at least two years of
the nation to be represented, age of at least 21
years, and a pledge of service to humanity.
