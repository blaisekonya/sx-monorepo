Article 13 - Directive Principles for
the Earth Federation
It is the aim of the Earth Federation to secure
certain other rights for all inhabitants within the
Earth Federation, but without immediate
guarantee of universal achievement and
enforcement. These rights are defined as
Directive Principles, obligating the Earth
Federation to pursue every reasonable means
for universal realization and implementation.
Directive Principles include the following:
13.01. Equal opportunity for useful employment
for everyone, with wages or remuneration
sufficient to assure human dignity.
13.02. Freedom of choice in work, occupation,
employment or profession.
13.03. Full access to information and to the
accumulated knowledge of the human race.
13.04. Free and adequate public education
available to everyone, extending to the preuniversity level; Equal opportunities for
elementary and higher education for all persons;
equal opportunity for continued education for all
persons throughout life; the right of any person
or parent to choose a private educational
institution at any time.
13.05. Free and adequate public health services
and medical care available to everyone
throughout life under conditions of free choice.
13.06. Equal opportunity for leisure time for
everyone; better distribution of the work load of
society so that every person may have equitable
leisure time opportunities.
13.07. Equal opportunity for everyone to enjoy
the benefits of scientific and technological
discoveries and developments.
13.08. Protection for everyone against the
hazards and perils of technological innovations
and developments.
13.09. Protection of the natural environment that
is the common heritage of humanity against
pollution, ecological disruption or damage that
might imperil life or lower the quality of life.
13.10. Conservation of those natural resources
of Earth that are limited so that present and
future generations may continue to enjoy life on
the planet Earth.
13.11. Assurance for everyone of adequate
housing, of adequate and nutritious food
supplies, of safe and adequate water supplies,
of pure air with protection of oxygen supplies
and the ozone layer, and in general for the
continuance of an environment that can sustain
healthy living for all.
13.12. Assure to each child the right to the full
realization of his or her potential.
13.13. Social Security for everyone to relieve the
hazards of unemployment, sickness, old age,
family circumstances, disability, natural
catastrophe, and technological change, and to
allow retirement with sufficient lifetime income
for living under conditions of human dignity
during older age.
13.14. Rapidly prohibit and eliminate
technological hazards and man-made
environmental disturbances that create dangers
to life on Earth.
13.15. Implementation of intensive programs to
discover, develop and institute safe alternatives
and practical substitutions for technologies that
are hazards or dangers to life.
13.16. Encouragement for cultural diversity;
encouragement for decentralized administration.
13.17. Freedom for peaceful self-determination
for minorities, refugees and dissenters.
13.18. Freedom for change of residence to
anywhere on Earth conditioned by provisions for
temporary sanctuaries in events of large
numbers of refugees, stateless persons, or
mass migrations.
13.19. Prohibition against the death penalty.
Article 14 - Safeguards and
Reservations
14.1. - Certain Safeguards
The Earth Federation shall operate to secure for
all nations and peoples within the Earth
Federation the safeguards that are defined
hereunder:
14.1.1. Guarantee to give full faith and credit to
the public acts, records, legislation and judicial
proceedings of the member nations within the
Earth Federation, consistent with the several
provisions of this World Constitution.
14.1.2. Assure freedom of choice within the
member nations and countries of the Earth
Federation to determine their internal political,
economic and social systems, consistent with
the guarantees and protections given under this
World Constitution to assure civil liberties and
human rights and a safe environment for life,
27
and otherwise consistent with the several
provisions of this World Constitution.
14.1.3. Grant asylum within the Earth Federation
for persons who might seek refuge from nations
that are not yet included within the Earth
Federation.
14.1.4. Grant to individuals and groups, after the
Earth Federation includes 90 percent of the
territory of Earth, to peacefully leave the
hegemony of the Earth Federation and to live in
suitable territory set aside by the Federation
neither restricted nor protected by the Earth
Federation, provided that such territory does not
extend beyond five percent of Earth’s habitable
territory, is kept completely disarmed and not
used as a base for inciting violence or
insurrection within or against the Earth
Federation or any member nation, and is kept
free of acts of environmental or technological
damage that seriously affect Earth outside such
territory.
14.2. - Reservation of Powers
The powers this World Constitution does not
delegate to the Earth Federation reserve to the
nations of the Earth Federation and to the
people of Earth.
