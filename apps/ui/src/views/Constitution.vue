Elections Administration may conduct elections
for members of the World Parliament to the
House of Peoples whenever feasible in relation
to the campaign for ratification of this World
Constitution.
5.6.11. Regular sessions of the House of
Peoples and House of Nations of the World
Parliament shall convene on the second Monday
of January of each year.
5.6.12. Each nation, according to its own
procedures, shall appoint or elect members of
the World Parliament to the House of Nations at
least thirty days prior to the date for convening
the World Parliament in January.
5.6.13. The House of Peoples together with the
House of Nations shall elect the members of the
World Parliament to the House of Counsellors
during the month of January after the general
