Article 18 - Amendments
18.1. Following completion of the first operative
stage of Earth Federation, amendments to this
World Constitution may be proposed for
consideration in two ways:
18.1.1. By a simple majority vote of any House
of the World Parliament.
18.1.2. By petitions signed by a total of 200,000
persons eligible to vote in world elections from a
total of at least twenty World Electoral and
Administrative Districts where the World
Constitution has received final ratification.
18.2. Adoption of any amendment proposed by
a House of the World Parliament is subject to an
absolute two-thirds majority vote of each of the
three Houses of the World Parliament voting
separately.
18.3. An amendment proposed by popular
petition requires deliberation and vote for
consideration by the House of Peoples. A simple
majority vote of the House of Peoples to
consider the amendment obliges each House to
a vote upon the proposed amendment. Adoption
of the amendment is subject to an absolute twothirds majority vote of each of the three Houses
of the World Parliament voting separately.
18.4. Periodically, but no later than ten years
after first convening the World Parliament for the
first operative stage of Earth Federation, and
every 20 years thereafter, the Members of the
World Parliament shall meet in special session
comprising a Constitutional Convention to
conduct a review of this World Constitution to
consider and propose possible amendments,
which for adoption is then subject to action
specified in Article 18.2.
18.5. If the first operative stage of Earth
Federation is not attained by the year 1995, then
the Provisional World Parliament, as provided
under Article 19, may convene another session
of the World Constituent Assembly to review the
Constitution for the Federation of Earth and
consider possible amendments according to
procedure the Provisional World Parliament
establishes.

18.6. Except by following the amendment
procedures specified herein, no part of this
World Constitution must be set aside,
suspended or subverted, neither for
emergencies nor caprice nor convenience.
