5.6.14. Bi-elections to fill vacancies shall be held
within three months from occurrence of the
vacancy or vacancies.
5.6.15. The World Parliament shall remain in
session for a minimum of nine months of each
year. World Parliament may take one or two
breaks during each year. Simple majority vote of
the House of Peoples and House of Nations
sitting jointly may decide times and durations of
the breaks.
5.6.16. Annual salaries for members of the
World Parliament of all three houses are the
same, except Members of the Presidium and of
the Executive Cabinet may receive additional
compensation commensurate with the additional
duties.
5.6.17. The World Parliament shall determine
salary schedules for members of the World
Parliament and for members of the Presidium
and of the Executive Cabinet.
