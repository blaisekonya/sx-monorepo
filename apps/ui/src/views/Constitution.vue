19.2. - Work of the Preparatory
Commissions
19.2.01. The Ratification Commission shall
campaign worldwide for the ratification of the
World Constitution, both to obtain preliminary
ratification by national governments, including
national legislatures, and to obtain final
ratification by people, including communities.
The ratification commission shall continue until
the full operative stage of Earth Federation is
reached.
19.2.02. The World Elections Commission shall
prepare a provisional global map of World
Electoral and Administrative Districts and
Regions that the World Parliament may revise
during the first or second operative stage of
Earth Federation, and shall prepare and proceed
with plans to obtain the election of Members of
the World Parliament to the House of Peoples
and to the House of Counsellors. In due course,
the World Parliament shall convert the World
Elections Commission into the World
Boundaries and Elections Administration.
19.2.03. After six months, in those countries
where national governments have not
responded favorably to the ratification call, the
Ratification Commission and the World Elections
Commission may proceed jointly to accomplish
both the ratification of the World Constitution by
direct popular referendum and concurrently the
election of Members of the World Parliament.
19.2.04. The Ratification Commission may also
submit the world constitution for ratification by
universities and colleges throughout the world.
19.2.05. The World Development Commission
