Article 12 - Bill of Rights for the
Citizens of Earth
The inhabitants of Earth who are within the
Earth Federation have certain inalienable rights
defined hereunder. It is mandatory for the World
Parliament, the World Executive, and all organs
and agencies of the Earth Federation to honor,
implement and enforce these rights, as well as
for the national governments of all member
nations in the Earth Federation to do likewise.
Individuals or groups suffering violation or
neglect of rights have full recourse through the
World Ombudsmus, the Enforcement System
and the World Courts for redress of grievances.
The inalienable rights include the following:
12.01. Equal rights for all citizens of the Earth
Federation, with no discrimination on grounds of
race, color, caste, nationality, sex, religion,
political affiliation, property, or social status.
12.02. Equal protection and equal application of
world laws for all citizens of the Earth
Federation.
12.03. Except as an incitement to violence,
armed riot or insurrection, freedom of expression
through the following: thought and conscience,
speech, press, writing, communication,
expression, publication, broadcasting,
telecasting, and cinema.
12.04. Freedom of assembly, association,
organization, petition, and peaceful
demonstration.
12.05. Freedom to vote without duress, and
freedom for political organization and
campaigning without censorship or
recrimination.
12.06. Freedom to profess, practice and
promote religious or religious beliefs or no
religion or religious belief.
12.07. Freedom to profess and promote political
beliefs or no political beliefs.
12.08. Freedom for investigation, research and
reporting.
12.09. Freedom to travel without passport or
visas or other forms of registration used to limit
travel between, among or within nations.
12.10. Prohibition against slavery, peonage,
involuntary servitude, and conscription of labor.
12.11. Prohibition against military conscription.
12.12. Safety of person from arbitrary or
unreasonable arrest, detention, exile, search or
seizure; requirement of warrants for searches
and arrests.
12.13. Prohibition against physical or
psychological duress or torture during any
period of investigation, arrest, detention or
imprisonment, and against cruel or unusual
punishment.
12.14. Rights of habeas corpus; no ex-post-facto
laws; no double jeopardy; right to refuse selfincrimination or the incrimination of another.
12.15. Prohibition against private armies and
paramilitary organizations as being threats to the
common peace and safety.
12.16. Safety of property from arbitrary seizure;
protection against exercise of the power of
eminent domain without reasonable
compensation.
12.17. Right to family planning and free public
assistance to achieve family planning objectives.
12.18. Right of privacy of person, family and
association; prohibition against surveillance as a
means of political control.
