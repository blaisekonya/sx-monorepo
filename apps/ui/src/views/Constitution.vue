Article 10 - The Enforcement System
10.1. - Basic Principles
The enforcement of world law and world
legislation applies directly to individuals.
Individuals are accountable for compliance with
world law and world legislation regardless of
whether the individuals are acting in their own
capacity or as agents or officials of governments
at any level or of the institutions of governments,
or as agents or officials of corporations,
organizations, associations or groups of any
kind.
10.1.2. When world law or world legislation or
decisions of the world courts are violated, the
Enforcement System shall operate to identify
and apprehend the violating individuals.
10.1.3. Any enforcement action must not violate
the civil and human rights guaranteed under this
World Constitution.
10.1.4. In the context of a non-military world
federation wherein all member nations shall
disarm from weapons of mass destruction as a
condition for joining and benefiting from the
world federation, subject to Article 17.3.8 and
17.4.6 of this World Constitution, the
Enforcement System shall enforce world law
and world legislation. The Earth Federation must
neither keep nor use weapons of mass
destruction.
10.1.5. The Enforcement System shall equip
Enforcement System agents only with
instruments of defense appropriate for the
apprehension of the individuals violating the
world law.
10.1.6. The Enforcement System shall conceive
and develop the enforcement of world law and
world legislation under this World Constitution
primarily as the processes of effective design
and administration of world law and world
legislation to serve the welfare of all people on
Earth, with equity and justice for all, in which the
resources of Earth and the funds and the credits
of the Earth Federation are used only to serve
peaceful human needs, and none used for
weapons of mass destruction or for war making
capabilities.
