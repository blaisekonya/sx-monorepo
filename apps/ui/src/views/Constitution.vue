8.1.1. The Integrative Complex comprises
certain administrative, research, planning and
facilitative agencies of the Earth Federation that
are particularly essential for the satisfactory
functioning of all or most aspects of the Earth
Federation. The Integrative Complex includes
the agencies listed under this Section, with the
proviso that the Presidium may recommend to
the World Parliament to add other agencies.
