Economic Development Organization to serve
all nations and people ratifying the world
constitution, and in particular less developed
countries, to begin functioning when the
Provisional Earth Federation is established.
19.2.06. The World Disarmament Commission
shall prepare plans for the organization of a
World Disarmament Agency, to begin
functioning when the Provisional Earth
Federation is established.
19.2.07. The World Problems Commission shall
prepare an agenda of urgent world problems,
with documentation, for possible action by the
Provisional World Parliament and Provisional
Earth Federation.
19.2.08. The Nominating Commission shall
