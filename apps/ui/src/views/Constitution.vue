House of Counsellors by a concurrent vote of
the other two houses.
5.6.05. The House of Counsellors may initiate
any legislative measure. The House of
Counsellors shall submit initiated measures to
the other two houses. House of Counsellors
initiated measures are subject to simple majority
vote of both the House of Peoples and House of
Nations to become effective, unless other voting
majority is required by some provision of this
World Constitution.
5.6.06. The House of Counsellors may introduce
an opinion or resolution on any measure
pending before either of the other two houses;
either of the other houses may request the
opinion of the House of Counsellors before
acting upon a measure.
5.6.07. Each house of the World Parliament
shall adopt its own detailed rules of procedure,
consistent with the procedures set forth in this
World Constitution. Each house shall design its
own rules to facilitate coordinated functioning of
the three houses.
5.6.08. Approval of appointments by the World
Parliament or any house thereof requires simple
majority vote, while removal for cause requires
absolute majority vote.
5.6.09. After World Parliament declares the full
operative stage of Earth Federation, the World
