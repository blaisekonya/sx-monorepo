19.5.7. The several preparatory commissions on
urgent world problems may reconstitute as
Administrative Departments of the Provisional
Earth Federation.
19.5.8. In all of its work and activities, the
Provisional Earth Federation shall function in
accordance with the provisions of this
Constitution for the Federation of Earth.
