5.2. Composition of the World
Parliament
5.2.1. The World Parliament is of three houses,
designated as follows:
 The House of Peoples, to represent the
people of Earth directly and equally;
 The House of Nations, to represent the
nations that are joined together in the Earth
Federation; and
 The House of Counsellors with particular
functions to represent the highest good and best
