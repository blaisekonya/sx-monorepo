administration and implementation, unless World
Parliament provides otherwise for specific
assignments.
7.2.11. The Presidium, in consultation with the
Executive Cabinet, may propose the creation of
other departments and agencies to have
ministerial status; and may propose the
alteration, combination or termination of existing
Departments and agencies of ministerial status.
Any creation, alteration, combination or
termination of departments or agencies is
subject to a simple majority vote of approval of
the three houses of the World Parliament in joint
session.
7.2.12. The World Parliament by absolute
majority vote of the three houses in joint session
may specify the creation of new departments or
agencies of ministerial status in the World
Administration, or may direct the World
Executive to alter, combine, or terminate existing
departments or agencies of ministerial status.
7.2.13. The Presidium and the World Executive
must not create, establish or maintain any
administrative or executive department or
agency for the purpose of circumventing World
Parliament control.
