6.1.7. To prepare and submit annually to the
World Parliament a comprehensive budget for
the operations of the Earth Federation, and to
prepare and submit periodically budget
projections over periods of several years.
6.1.8. To define and propose priorities for world
legislation and budgetary allocations.
6.1.9. To be held accountable to the World
Parliament for the expenditures of
appropriations the World Parliament makes in
accordance with approved and longer term
budgets, subject to revisions the World
Parliament approves.
